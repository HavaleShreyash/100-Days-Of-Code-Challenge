def rotate(matrix): 
    #code here
    for i in range(len(matrix)):
        for j in range(i+1,len(matrix)):
            matrix[i][j],matrix[j][i]=matrix[j][i],matrix[i][j]
    res=[]
    for i in range(len(matrix)-1,-1,-1):
        res.append(matrix[i])
    matrix[::]=res[::]
    return res

